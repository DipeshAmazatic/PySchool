"""You can make use of the len(x) function to find out the number of characters
in a string.
"""
WORD = "Hello Google!"
# number of characters stored in the variable greeting
NUMBER_OF_CHAR = len(WORD)

# repeat the greetings based on the number of character in 'greeting'
WORDS = WORD * NUMBER_OF_CHAR
print(NUMBER_OF_CHAR)
print(WORDS)
